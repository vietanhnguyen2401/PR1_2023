public class TriangleArea {
    public static void main(String[] args) {
        int base = 3;
        double height = 1.5;
        double area = (height*base)/2;

        System.out.println("The triangle's base is "+base+" (cm) and height is "+height+" (cm).");
        System.out.print("It's area (cm2) is: " +area );
    }
}