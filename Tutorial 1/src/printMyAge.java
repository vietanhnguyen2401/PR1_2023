public class printMyAge {
    public static void main(String[] args) {
        int yob = 2001;
        int year = 2023;
        int age = year - yob;
        System.out.println("I was born in 2001. This year is 2023");
        System.out.println("Therefore, my name is: ");
        System.out.println(age);
    }
}
